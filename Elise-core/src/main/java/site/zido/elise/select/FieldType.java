package site.zido.elise.select;

/**
 * The enum Field type.
 *
 * @author zido
 */
public enum FieldType {
    /**
     * Number field type.
     */
    NUMBER,
    /**
     * Text field type.
     */
    TEXT,
    /**
     * Rich field type.
     */
    RICH,
    /**
     * Origin field type.
     */
    ORIGIN,
    /**
     * Xml field type.
     */
    XML,
    /**
     * Html field type.
     */
    HTML,
    /**
     * Bytes field type.
     */
    BYTES,
    ;
}
