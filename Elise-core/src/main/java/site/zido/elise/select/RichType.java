package site.zido.elise.select;

/**
 * The enum Rich type.
 *
 * @author zido
 */
public enum RichType {
    /**
     * Text rich type.
     */
    TEXT,
    /**
     * Image rich type.
     */
    IMAGE,
    /**
     * Audio rich type.
     */
    AUDIO,
    /**
     * Video rich type.
     */
    VIDEO,
    /**
     * Link rich type.
     */
    LINK,
    /**
     * Content start rich type.
     */
    CONTENT_START,
    /**
     * Content end rich type.
     */
    CONTENT_END
}
